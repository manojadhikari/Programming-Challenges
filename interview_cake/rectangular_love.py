#Find the overlap of two rectangles

import unittest


def find_rectangular_overlap(rect1, rect2):

    print(rect1)

    def overlap(x1, end1, x2, end2):
        if x2 >= end1 or x1 >= end2:
            return (None,None)

        if x2 > x1:
            return (x2, min(end1, end2))

        return (x1, min(end1, end2))

    # Calculate the overlap between the two rectangles
    left_x = 'left_x'
    width = 'width'
    bottom_y = 'bottom_y'
    height = 'height'

    x_overlap, end1 = overlap(rect1[left_x], rect1[left_x] + rect1[width], rect2[left_x], rect2[left_x] + rect2[width])
    y_overlap, end2 = overlap(rect1[bottom_y], rect1[bottom_y] + rect1[height], rect2[bottom_y], rect2[bottom_y] + rect2[height])


    if x_overlap and y_overlap:
        return {
            'left_x': x_overlap,
            'bottom_y': y_overlap,
            'width': end1 - x_overlap,
            'height': end2 - y_overlap
            }

    return {
            'left_x': None,
            'bottom_y': None,
            'width': None,
            'height': None
            }


















